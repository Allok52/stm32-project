#include "i2c_2.h"

static void I2C_2_Delay(void)
{
	uint8_t i;

	for (i = 0; i < 20; i++);
}

void I2C_2_Start(void)
{
	/* 当SCL高电平时，SDA出现一个下跳沿表示I2C总线启动信号 */
	I2C_2_SDA_HIGH();
//	for(uint16_t j=0;j<100;j++) ;
	I2C_2_SCL_HIGH();
	I2C_2_Delay();
	I2C_2_SDA_LOW();
	I2C_2_Delay();
	I2C_2_SCL_LOW();
	I2C_2_Delay();
}


void I2C_2_Stop(void)
{
	/* 当SCL高电平时，SDA出现一个上跳沿表示I2C总线停止信号 */
	I2C_2_SDA_LOW();
	I2C_2_SCL_HIGH();
	I2C_2_Delay();
	I2C_2_SDA_HIGH();
}

/**
  * 函数功能: CPU向I2C总线设备发送8bit数据
  * 输入参数: Byte ： 等待发送的字节
  * 返 回 值: 无
  * 说    明：无
  */
void I2C_2_SendByte(uint8_t Byte)
{
	uint8_t i;

	/* 先发送字节的高位bit7 */
	for (i = 0; i < 8; i++)
	{		
		if (Byte & 0x80)
		{
			I2C_2_SDA_HIGH();
		}
		else
		{
			I2C_2_SDA_LOW();
		}
		I2C_2_Delay();
		I2C_2_SCL_HIGH();
		I2C_2_Delay();	
		I2C_2_SCL_LOW();
		if (i == 7)
		{
			I2C_2_SDA_HIGH(); // 释放总线
		}
		Byte <<= 1;	/* 左移一个bit */
		I2C_2_Delay();
	}
}


/**
  * 函数功能: CPU从I2C总线设备读取8bit数据
  * 输入参数: 无
  * 返 回 值: 读到的数据
  * 说    明：无
  */
uint8_t I2C_2_ReadByte(uint8_t ack)
{
	uint8_t i;
	uint8_t value;

	/* 读到第1个bit为数据的bit7 */
	value = 0;
	for (i = 0; i < 8; i++)
	{
		value <<= 1;
		I2C_2_SCL_HIGH();
		I2C_2_Delay();
		if (I2C_2_SDA_READ())
		{
			value++;
		}
		I2C_2_SCL_LOW();
		I2C_2_Delay();
	}
  if(ack==0)
		I2C_2_NAck();
	else
		I2C_2_Ack();
	return value;
}

/**
  * 函数功能: CPU产生一个时钟，并读取器件的ACK应答信号
  * 输入参数: 无
  * 返 回 值: 返回0表示正确应答，1表示无器件响应
  * 说    明：无
  */
uint8_t I2C_2_WaitAck(void)
{
	uint8_t re;

	I2C_2_SDA_HIGH();	/* CPU释放SDA总线 */
	I2C_2_Delay();
	I2C_2_SCL_HIGH();	/* CPU驱动SCL = 1, 此时器件会返回ACK应答 */
	I2C_2_Delay();
	if (I2C_2_SDA_READ())	/* CPU读取SDA口线状态 */
	{
		re = 1;
	}
	else
	{
		re = 0;
	}
	I2C_2_SCL_LOW();
	I2C_2_Delay();
	return re;
}

/**
  * 函数功能: CPU产生一个ACK信号
  * 输入参数: 无
  * 返 回 值: 无
  * 说    明：无
  */
void I2C_2_Ack(void)
{
	I2C_2_SDA_LOW();	/* CPU驱动SDA = 0 */
	I2C_2_Delay();
	I2C_2_SCL_HIGH();	/* CPU产生1个时钟 */
	I2C_2_Delay();
	I2C_2_SCL_LOW();
	I2C_2_Delay();
	I2C_2_SDA_HIGH();	/* CPU释放SDA总线 */
}

/**
  * 函数功能: CPU产生1个NACK信号
  * 输入参数: 无
  * 返 回 值: 无
  * 说    明：无
  */
void I2C_2_NAck(void)
{
	I2C_2_SDA_HIGH();	/* CPU驱动SDA = 1 */
	I2C_2_Delay();
	I2C_2_SCL_HIGH();	/* CPU产生1个时钟 */
	I2C_2_Delay();
	I2C_2_SCL_LOW();
	I2C_2_Delay();	
}

/**
  * 函数功能: 配置I2C总线的GPIO，采用模拟IO的方式实现
  * 输入参数: 无
  * 返 回 值: 无
  * 说    明：无
  */
void I2C_2_InitGPIO(void)
{
  GPIO_InitTypeDef GPIO_InitStruct;
  
  /* 打开GPIO时钟 */
	I2C_2_GPIO_CLK_ENABLE();

  GPIO_InitStruct.Pin = I2C_2_SCL_PIN|I2C_2_SDA_PIN;
  GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_OD;
  GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_MEDIUM;
  HAL_GPIO_Init(I2C_2_GPIO_PORT, &GPIO_InitStruct);

  /* 给一个停止信号, 复位I2C总线上的所有设备到待机模式 */
  I2C_2_Stop();
}

/**
  * 函数功能: 检测I2C总线设备，CPU向发送设备地址，然后读取设备应答来判断该设备是否存在
  * 输入参数: _Address：设备的I2C总线地址
  * 返 回 值: 返回值 0 表示正确， 返回1表示未探测到
  * 说    明：在访问I2C设备前，请先调用 I2C_CheckDevice() 检测I2C设备是否正常，该函数会配置GPIO
  */
uint8_t I2C_2_CheckDevice(uint8_t _Address)
{
	uint8_t ucAck;

	I2C_2_InitGPIO();		/* 配置GPIO */	
	I2C_2_Start();		/* 发送启动信号 */
	/* 发送设备地址+读写控制bit（0 = w， 1 = r) bit7 先传 */
	I2C_2_SendByte(_Address | I2C_WR);
	ucAck = I2C_2_WaitAck();	/* 检测设备的ACK应答 */
	I2C_2_Stop();			/* 发送停止信号 */
	return ucAck;
}

